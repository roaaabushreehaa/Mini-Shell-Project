#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*p;
	int		i;
	size_t	len;

	i = 0;
	len = ft_strlen((char *)s);
	p = malloc(sizeof(char) * (len + 1));
	if (p == NULL)
		return (NULL);
	while (s[i] != '\0')
	{
		p[i] = s[i];
		i++;
	}
	p[i] = '\0';
	return (p);
}
